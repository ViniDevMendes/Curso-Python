n1 = int(input('Digite quantos metros você quer transformar em centimetros: '))
s = n1 * 100
print('Convertendo {} metros em centimetros, o resultado é {} centimetros'.format(n1, s))

n2 = int(input('Digite quantos metros você quer transformar em milímetros: '))
ss = n2 * 1000
print('Convertendo {} metros em minímetros, o resultado é {}'.format(n2, ss))